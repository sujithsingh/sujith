package finalpractise;

public class Controlstatements {
	public static void main(String[] args) {

		for (int i = 1; i <= 100; i++) {

			if (i == 5) {
				System.out.println(i);
				continue;

			}

		}

		for (int i = 1; i <= 3; i++) {
			for (int j = 1; j < 3; j++) {

				System.out.println(j);

			}

		}

	}

}
